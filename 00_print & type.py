print(type("hola Mundo"))
# el tipo es str (string) o cadena de texto
print(type(5))
# el tipo es int (entero) o numero entero
print(type(5.5))
# el tipo es float (flotante) o numero decimal
print(type(5 + 3j))
# el tipo es complex (complejo) o numero complejo
print(type(True))
# el tipo es bool (booleano) o verdadero/falso